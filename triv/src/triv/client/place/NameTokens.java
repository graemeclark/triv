package triv.client.place;

/**
 * Defines methods for returning the name tokens for different presenters.
 * @author Graeme Clark
 *
 */
public class NameTokens
{
  public static final String inPage = "in";
  public static final String exePage = "exe";

  public static String getInputPage()
  {
    return inPage;
  }

  public static String getExecutionPage()
  {
    return exePage;
  }

}
